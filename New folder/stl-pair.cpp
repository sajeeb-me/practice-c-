#include<bits/stdc++.h>
using namespace std;

int main()
{
    pair<string, int> a;
    a.first = "name";
    a.second = 20;

    cout << "Pair a = " << a.first << ", " << a.second << "\n";

    pair<int, int> b;
    b.first = 20;
    b.second = 30;

    cout << "Pair b = " << b.first << ", " << b.second << "\n";

    return 0;
}
