#include<bits/stdc++.h>

using namespace std;

int main()
{
    int x;
    cin >> x;
    switch(x)
    {
    case 2:
        cout << "Number: 2\n";
        break;
    case 5:
        cout << "Number: 5\n";
        break;
    default:
        cout << "Invalid\n";
        break;
    }

    return 0;
}
